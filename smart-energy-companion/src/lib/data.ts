// The synthetic dataset is for the year 2025. We anchor a fixed "now" so the
// live view always shows an interesting summer-midday moment for the demo.
export const REFERENCE_NOW = "2025-06-20T13:00:00";

// ---- data source: the enpal backend -----------------------------------------
// The frontend keeps no dataset of its own, every byte is fetched from the
// enpal server's raw-data API. Point ENPAL_API_URL at the backend (defaults to
// the local dev server). All the computation below is unchanged; only the
// *source* of the raw JSON moved from local files to the server.

const API_BASE = (
  process.env.ENPAL_API_URL ?? "http://localhost:8000/api/v1"
).replace(/\/$/, "");

async function fetchJson<T>(path: string): Promise<T> {
  const res = await fetch(`${API_BASE}${path}`, { cache: "no-store" });
  if (!res.ok) {
    throw new Error(
      `Enpal API ${path} failed: ${res.status} ${res.statusText}. ` +
        `Is the backend running at ${API_BASE}?`
    );
  }
  return (await res.json()) as T;
}

/**
 * Ensure the reference data (and, when an id is given, that household's
 * timeseries) is loaded from the backend into the in-memory caches. Call this
 * and `await` it before invoking any of the synchronous accessors/derived
 * views below. Cheap to call repeatedly, each dataset is fetched once.
 */
export async function ensureData(id?: string): Promise<void> {
  if (!_households) {
    const [households, tariffs, bills, insights, prices] =
      await Promise.all([
        fetchJson<Household[]>("/data/households"),
        fetchJson<Tariff[]>("/data/tariffs"),
        fetchJson<MonthlyBill[]>("/data/monthly_bills"),
        fetchJson<InsightEvent[]>("/data/insight_events"),
        fetchJson<{ prices: SpotPrice[] }>("/data/dynamic_prices"),
      ]);
    _households = households;
    _tariffs = tariffs;
    _bills = bills;
    _insights = insights;
    _prices = prices.prices;
  }
  if (id && !_timeseries.has(id)) {
    _timeseries.set(id, await fetchJson<Timeseries>(`/data/timeseries/${id}`));
  }
}

// ---- chatbot: profile-based suggested questions (from the enpal server) ------

export interface SuggestedQuestion {
  id: string;
  text: string;
  source: string; // "profile" | "popular"
}

/**
 * Pre-defined questions for a household, generated by the enpal chat service
 * from that household's own profile (assets, tariff, anomalies). Falls back to
 * the static popular list if the backend is unreachable.
 */
export async function getSuggestedQuestions(id: string): Promise<SuggestedQuestion[]> {
  try {
    const data = await fetchJson<{ questions: SuggestedQuestion[] }>(
      `/chat/suggested-questions?household_id=${encodeURIComponent(id)}`
    );
    if (data?.questions?.length) return data.questions;
  } catch (err) {
    console.error("suggested-questions fetch failed, using fallback:", err);
  }
  return POPULAR_QUESTIONS.map((q, i) => ({
    id: `popular-${i}`,
    text: q.q,
    source: "popular",
  }));
}

function loaded<T>(value: T | null, what: string): T {
  if (value === null) {
    throw new Error(`${what} not loaded, call \`await ensureData()\` first.`);
  }
  return value;
}

